number1 = int(input("Please enter a number: "))
number2 = int(input("Please enter another number: "))

print (str(number1) + "+" + str(number2) + "=" + str(number1 + number2))
print (str(number1) + "-" + str(number2) + "=" + str(number1 - number2))
print (str(number1) + "*" + str(number2) + "=" + str(number1 * number2))
print (str(number1) + "/" + str(number2) + "=" + str(number1 / number2))